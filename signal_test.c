#include <signal.h>
#include <stdio.h>
#include <unistd.h>

void	my_sig_handler(int sig)
{
	if (sig == SIGINT)
	{
		fprintf(stdout, "I received SIGINT\n");
	}
}
int main()
{
	fprintf(stdout, "PID: %d\n", getpid());
	signal(SIGINT, my_sig_handler);
	sleep(3);
}